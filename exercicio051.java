// 51) Faça um programa que exiba os números de 1 a 10 usando o loop "do-while".


package listaexercicios2;

public class exercicio051 {
	
	public static void main(String[] args) {
        int i = 1;
        
        do {
            System.out.println(i);
            i++;
        } while (i <= 10);
    }

}
